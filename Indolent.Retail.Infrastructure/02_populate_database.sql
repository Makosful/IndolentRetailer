FROM (SELECT o.id AS order_id, p.id AS product_id
      FROM "orders" AS o
               CROSS JOIN LATERAL ( SELECT "products"."id" FROM "products" ORDER BY random()) p
      ORDER BY random()) sub
LIMIT ((SELECT count(*) FROM "orders") * 4);
