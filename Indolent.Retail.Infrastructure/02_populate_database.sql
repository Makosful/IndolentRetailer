SELECT uuid
FROM "customers"
         CROSS JOIN LATERAL (SELECT "id" FROM "products") p
ORDER BY random()
LIMIT (SELECT (((SELECT count(*) FROM "customers") * 2) + ((SELECT count(*) FROM "products") * 5)));


INSERT INTO "order_products" ("order_id", "product_id", "amount")
